export function getBlogPost(id) {
  return blogPosts.find((post) => post.id === id)
}

export function getBlogPostsByCategory(category) {
  return blogPosts.filter((post) => post.category === category)
}

export function getRelatedPosts(currentPostId, limit = 3) {
  const currentPost = getBlogPost(currentPostId)
  if (!currentPost) return []

  return blogPosts.filter((post) => post.category === currentPost.category && post.id !== currentPostId).slice(0, limit)
}
